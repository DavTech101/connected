import express from 'express';

//##########################################################################################
// EXPRESS SERVER
//##########################################################################################
const app = express();

//##########################################################################################
// ROUTES
//##########################################################################################
app.get('/', (req, res) => res.send('Express SRC on Vercel'));

//##########################################################################################
// LISTEN
//##########################################################################################
app.listen(5000, () => console.log('Server ready on port 5000.'));

export default app;
